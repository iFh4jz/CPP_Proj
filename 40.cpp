#include <iostream>
#include <cstring>
using namespace std;
   
int main()
{
    char s[1000];
    while(gets(s)!= NULL )
    {
        int cnt1=0;
        int cnt2=0;
        int cnt3=0;
        int cnt4=0;
        for(int i=0;i<strlen(s);i++)
        {
            if(s[i]<='z'&&s[i]>='a'||s[i]<='Z'&&s[i]>='A')
            cnt1++;
            else if(s[i]==' ')
            cnt2++;
            else if(s[i]<='9'&&s[i]>='0')
            cnt3++;
            else
            cnt4++;
        }
        printf("%d\n",cnt1);
        printf("%d\n",cnt2);
        printf("%d\n",cnt3);
        printf("%d\n",cnt4);
    }
    return 0;
}
