/*
 *题目描述
实现删除字符串中出现次数最少的字符，若多个字符出现次数一样，则都删除。输出删除这些单词后的字符串，字符串中其它字符保持原来的顺序。 
输入描述:
字符串只包含小写英文字母, 不考虑非法输入，输入的字符串长度小于等于20个字节。

输出描述:
删除字符串中出现次数最少的字符后的字符串。

示例1
输入
abcdd
输出
dd
 * */
#include <iostream>
#include <string>
#include <vector>
#include <algorithm>
using namespace std;
int main()
{
    string s;
    while(cin>>s)
    {
        int a[26]={0};
        int n=s.size();
        for(int i=0;i<n;++i)
        {
            a[s[i]-'a']++;
        }
        int mi=a[s[0]-'a'];//出现最少的字符次数
        for(int i=1;i<n;++i)
        {
            if (a[s[i]-'a']<mi)
            {
                mi=a[s[i]-'a'];//出现最少的字符次数
            }
        }
        for(int i=0;i<n;++i)
        {
            if (a[s[i]-'a']>mi)
                cout<<s[i];      
        }
        cout<<endl;
    }
    return 0;
}
